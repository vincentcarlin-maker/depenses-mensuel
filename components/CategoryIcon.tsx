interface CategoryIconProps {
  category: Category | string;
  size?: 'xs' | 'sm' | 'md' | 'lg';
}

const CategoryIcon: React.FC<CategoryIconProps> = ({ category, size = 'md' }) => {
  const config = categoryConfig[category];
  
  const iconSizeClasses = {
      xs: 'h-3 w-3',
      sm: 'h-4 w-4',
      md: 'h-5 w-5',
      lg: 'h-6 w-6',
  };

  const wrapperSizeClasses = {
    xs: 'w-5 h-5',
    sm: 'w-8 h-8',
    md: 'w-10 h-10',
    lg: 'w-12 h-12',
  };
  
  const IconComponent = config ? config.icon : QuestionMarkIcon;
  const bgColor = config ? config.bgColor : 'bg-gray-400';

  return (
    <div className={`flex items-center justify-center rounded-full text-white ${bgColor} ${wrapperSizeClasses[size]}`}>
      <IconComponent className={iconSizeClasses[size]} />
    </div>
  );
};

